// أداة لبناء مجموعة ليكرت اختبارية
function makeGroup(scale: string[], items: Array<{ text: string; top: string }>): LikertGroupResult {
  return {
    groupId: 'g', groupName: 'محور', mean: 0, stdDev: 0, level: 'High',
    scale: { labels: scale },
    questions: items.map((it, i) => ({
      index: i + 1, text: it.text, direction: 'positive', mean: 0, stdDev: 0, level: 'High',
      responses: scale.map(l => ({ label: l, count: l === it.top ? 90 : 1, percentage: l === it.top ? 90 : 2 })),
    })),
  } as unknown as LikertGroupResult;
}

describe('generateGroupComment — الصياغة والقطبية', () => {
  it('إيجابي: يسرد بصيغة الغائب جمع دون "يرون أن"', () => {
    const c = TableCommentService.generateGroupComment(
      makeGroup(['نعم', 'أحياناً', 'لا'], [{ text: 'أساعد الآخرين عند الحاجة', top: 'نعم' }])
    );
    expect(c).toContain('من أفراد عينة البحث يساعدون الآخرين عند الحاجة');
    expect(c).not.toContain('يرون أن يساعدون');
  });

  it('أحياناً: تُدرَج بعد الفعل المُسنَد للمستجيبين', () => {
    const c = TableCommentService.generateGroupComment(
      makeGroup(['نعم', 'أحياناً', 'لا'], [{ text: 'أتكلم عن مشاعري بدلا من الشجار', top: 'أحياناً' }])
    );
    expect(c).toContain('يتكلمون أحياناً عن مشاعرهم بدلا من الشجار');
  });

  it('سلبي (لا): نفي الفعل', () => {
    const c = TableCommentService.generateGroupComment(
      makeGroup(['نعم', 'أحياناً', 'لا'], [{ text: 'يهددني والداي بالطرد من المنزل', top: 'لا' }])
    );
    expect(c).toContain('لا يهددهم والديهم بالطرد من المنزل');
  });
});
